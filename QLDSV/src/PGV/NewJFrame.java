    private void btnTaoLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoLoginActionPerformed
        boolean check = true;
        if (txtTenLogin.getText().length() == 0) {
            lbLogin.setForeground(Color.red);
            lbLogin.setText("Tên đăng nhập không được bỏ trống");
            check = false;
            txtTenLogin.requestFocus();
            return;
        } else {
            lbLogin.setForeground(Color.white);
        }
        if (!checkKhoangTrang(txtTenLogin.getText().trim())) {
            lbLogin.setForeground(Color.red);
            lbLogin.setText("Tên đăng nhập không chứa khoảng trắng");
            check = false;
            txtTenLogin.requestFocus();
            return;
        } else {
            lbLogin.setForeground(Color.white);
        }
        if (check) {

            try {
                Connection conn = ConnectToDB.getConnection(USER_NAME, PASS_WORD);
                CallableStatement ps = conn.prepareCall("{Call SP_TAOLOGIN (?,?,?,?)}");
                ps.setString(1, txtTenLogin.getText().toUpperCase());
                ps.setString(2, String.valueOf(txtMKLogin.getPassword()));
                ps.setString(3, cbMaUser.getSelectedItem().toString());
                ps.registerOutParameter(4, Types.INTEGER);

                ps.execute();

                int n = ps.getInt(4);

                if (n == 0) {
                    JOptionPane.showMessageDialog(this, "Tạo tài khoản thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);

                } else if (n == 1) {
                    JOptionPane.showMessageDialog(this, "Trùng tên đăng nhập");
                } else {
                    JOptionPane.showMessageDialog(this, "Người dùng đã có tài khoản");
                }

                ps.close();

            } catch (SQLException se) {
                se.printStackTrace();
                System.out.println(" Lỗi Dang Ki Tai Khoan");
            }

        }
        txtTenLogin.setText("");
        txtMKLogin.setText("");
        cbMaUser.removeAllItems();
    }//GEN-LAST:event_btnTaoLoginActionPerformed

