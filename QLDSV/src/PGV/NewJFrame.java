    private void cbLoaiDoiTuongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbLoaiDoiTuongActionPerformed
        if (cbLoaiDoiTuong.getSelectedItem() != null) {
            String loai = cbLoaiDoiTuong.getSelectedItem().toString();
            if (loai.equals("Table")) {
                cbDoiTuong.removeAllItems();
                cbQuyen.removeAllItems();
                getDataTableName();
                for (Table table : listTable) {
                    cbDoiTuong.addItem(table.getTenTable());
                }
                for (String s : quyenTable_Views()) {
                    cbQuyen.addItem(s);
                }
            } else if (loai.equals("Stored Procedured")) {
                cbDoiTuong.removeAllItems();
                cbQuyen.removeAllItems();
                getDataSPName();
                for (StoredProcedured sp : listSP) {
                    cbDoiTuong.addItem(sp.getTenSP());
                }
                for (String s : quyenStoredProcedured()) {
                    cbQuyen.addItem(s);
                }
            } else {
                cbDoiTuong.removeAllItems();
                cbQuyen.removeAllItems();
                getDataViewsName();
                for (Views views : listViews) {
                    cbDoiTuong.addItem(views.getTenViews());
                }
                for (String s : quyenTable_Views()) {
                    cbQuyen.addItem(s);
                }
            }

        }
    }//GEN-LAST:event_cbLoaiDoiTuongActionPerformed

