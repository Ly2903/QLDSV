    private void btnDoiMatKhauActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoiMatKhauActionPerformed
        boolean check = true;
        String newPW = String.valueOf(txtNewPW.getPassword());
        String confirmPW = String.valueOf(txtConfirmPW.getPassword());
        if (cbLoginDoiMK.getSelectedItem() == null) {
            lbLoginDoiMK.setForeground(Color.red);
            lbLoginDoiMK.setText("Tên tài khoản không được bỏ trống");
            check = false;
            cbLoginDoiMK.requestFocus();
            return;
        } else {
            lbLoginDoiMK.setForeground(Color.white);
        }
        if (newPW.length() == 0) {
            lbNewPW.setForeground(Color.red);
            lbNewPW.setText("Mật khẩu không được bỏ trống");
            check = false;
            txtNewPW.requestFocus();
            return;
        } else {
            lbNewPW.setForeground(Color.white);
        }
        if (!checkKhoangTrang(newPW)) {
            lbNewPW.setForeground(Color.red);
            lbNewPW.setText("Mật khẩu không được có khoảng trắng");
            check = false;
            txtNewPW.requestFocus();
            return;
        } else {
            lbNewPW.setForeground(Color.white);
        }

        if (!confirmPW.equals(newPW)) {
            lbConfirmPW.setForeground(Color.red);
            lbConfirmPW.setText("Mật khẩu xác nhận không hợp lệ");
            check = false;
            txtConfirmPW.requestFocus();
            return;
        } else {
            lbConfirmPW.setForeground(Color.white);
        }
        if (check) {
            try {
                Connection conn = ConnectToDB.getConnection(USER_NAME, PASS_WORD);
                CallableStatement ps = conn.prepareCall("{call SP_DOIMATKHAU (?,?)}");
                ps.setString(1, cbLoginDoiMK.getSelectedItem().toString());
                ps.setString(2, String.valueOf(txtNewPW.getPassword()));
                ps.execute();
                if (cbLoginDoiMK.getSelectedItem().toString().equalsIgnoreCase(USER_NAME)) {
                    JOptionPane.showMessageDialog(this, "Đổi mật khẩu thành công! \n"
                            + "\n"
                            + " Vui lòng đăng nhập lại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                    new DANGNHAP().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Thay đổi mật khẩu thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
                ps.close();
            } catch (Exception e) {
                e.printStackTrace();
            }

            cbLoginDoiMK.removeAllItems();
            txtNewPW.setText("");
            txtConfirmPW.setText("");

        }

    }//GEN-LAST:event_btnDoiMatKhauActionPerformed

